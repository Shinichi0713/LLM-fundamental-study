    total_loss = 0
    for batch in train_dataloader:
        # バッチをデバイスに移動
        inputs = {k: v.to(device) for k, v in batch.items() if k != "offset_mapping"}

        # 順伝播
        outputs = model(**inputs)
        loss = outputs.loss

        # 逆伝播
        loss.backward()
        optimizer.step()
        scheduler.step()
        optimizer.zero_grad()

        total_loss += loss.item()

