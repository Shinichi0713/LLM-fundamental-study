import matplotlib.pyplot as plt
import numpy as np

def visualize_integer_ideal(n, xmin=-10, xmax=10):
    """
    整数環 ℤ のイデアル nℤ を可視化する。
    nℤ = { ..., -2n, -n, 0, n, 2n, ... }
    """
    # 整数点の生成
    integers = np.arange(xmin, xmax + 1)
    ideal_multiples = integers[integers % n == 0]
    non_ideal = integers[integers % n != 0]

    plt.figure(figsize=(10, 2))
    # イデアルに属する点（赤）
    plt.scatter(ideal_multiples, np.zeros_like(ideal_multiples),
               color='red', s=100, label=f'{n}ℤ (ideal)', zorder=3)
    # イデアルに属さない点（青）
    plt.scatter(non_ideal, np.zeros_like(non_ideal),
               color='blue', s=50, label=f'ℤ ∖ {n}ℤ', zorder=2)

    plt.axhline(0, color='black', linewidth=0.5)
    plt.yticks([])
    plt.xlabel('Integers ℤ')
    plt.title(f'Visualization of the ideal {n}ℤ in the integer ring ℤ')
    plt.legend()
    plt.grid(True, linestyle='--', alpha=0.7)
    plt.show()

# 例：偶数全体 2ℤ
visualize_integer_ideal(2)

def visualize_quotient_ring_mod_n(n):
    """
    剰余環 ℤ/nℤ を円周上の点として可視化する。
    イデアル nℤ で割ると、0,1,...,n-1 の n 個の元になる。
    """
    angles = np.linspace(0, 2*np.pi, n, endpoint=False)
    x = np.cos(angles)
    y = np.sin(angles)

    plt.figure(figsize=(6, 6))
    plt.scatter(x, y, s=100, color='red', zorder=3)
    for i in range(n):
        plt.text(x[i]*1.1, y[i]*1.1, str(i), fontsize=12,
                 ha='center', va='center')

    circle = plt.Circle((0,0), 1, fill=False, color='black', linestyle='--')
    plt.gca().add_artist(circle)
    plt.axis('equal')
    plt.title(f'Visualization of the quotient ring ℤ/{n}ℤ (mod {n} world)')
    plt.xlabel('Re')
    plt.ylabel('Im')
    plt.grid(True, linestyle='--', alpha=0.7)
    plt.show()

# 例：ℤ/5ℤ
visualize_quotient_ring_mod_n(5)

import matplotlib.pyplot as plt
import networkx as nx

def visualize_finite_poset_with_maximal():
    """
    有限半順序集合（ハッセ図）で、極大元のイメージを可視化する。
    ここでは Zorn の補題が自明に成り立つ有限の場合を例示。
    """
    # 半順序集合の例（ハッセ図）
    # 要素: a < b, a < c, b < d, c < d
    # d が極大元
    G = nx.DiGraph()
    G.add_edges_from([('a', 'b'), ('a', 'c'), ('b', 'd'), ('c', 'd')])

    pos = {
        'a': (0, 0),
        'b': (-1, 1),
        'c': (1, 1),
        'd': (0, 2)
    }

    plt.figure(figsize=(6, 6))
    nx.draw_networkx_nodes(G, pos, node_color='lightblue', node_size=800)
    nx.draw_networkx_labels(G, pos, font_size=12, font_weight='bold')
    nx.draw_networkx_edges(G, pos, edge_color='gray', arrows=True, arrowsize=20)

    # 極大元 d を強調
    nx.draw_networkx_nodes(G, pos, nodelist=['d'], node_color='red', node_size=800)

    plt.title("The concept of maximal elements in finite partially ordered sets (Zorn's lemma holds trivially)")
    plt.axis('off')
    plt.show()

visualize_finite_poset_with_maximal()

def visualize_chain_and_upper_bound():
    """
    半順序集合の鎖と、その上界のイメージを可視化する。
    """
    # 半順序集合の例
    # 要素: a < b < c < d, かつ e は a,b,c,d と比較不能だが、d の上にある
    G = nx.DiGraph()
    G.add_edges_from([('a', 'b'), ('b', 'c'), ('c', 'd')])
    # e は d の上にあるが、順序関係はない（図では上に配置）

    pos = {
        'a': (0, 0),
        'b': (0, 1),
        'c': (0, 2),
        'd': (0, 3),
        'e': (1, 4)
    }

    plt.figure(figsize=(6, 6))
    nx.draw_networkx_nodes(G, pos, node_color='lightblue', node_size=800)
    nx.draw_networkx_labels(G, pos, font_size=12, font_weight='bold')
    nx.draw_networkx_edges(G, pos, edge_color='gray', arrows=True, arrowsize=20)

    # 鎖 a < b < c < d を強調
    chain_nodes = ['a', 'b', 'c', 'd']
    nx.draw_networkx_nodes(G, pos, nodelist=chain_nodes, node_color='orange', node_size=800)
    # 上界 e を強調
    nx.draw_networkx_nodes(G, pos, nodelist=['e'], node_color='green', node_size=800)

    plt.title("鎖（オレンジ）とその上界（緑）のイメージ")
    plt.axis('off')
    plt.show()

visualize_chain_and_upper_bound()

