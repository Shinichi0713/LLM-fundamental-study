import json
import random

# validation.json を読み込む
with open("/content/labels/validation.json", "r") as f:
    data = json.load(f)

# 1. label ごとにエントリをグループ化
label_to_entries = {}
for entry in data:
    label = entry["label"]
    if label not in label_to_entries:
        label_to_entries[label] = []
    label_to_entries[label].append(entry)

# 2. 各ラベルについて、ランダムにペアを生成
list_pair = []
for label, entries in label_to_entries.items():
    if len(entries) < 2:
        # 同じラベルが1つしかない場合はスキップ
        continue

    # エントリをシャッフル
    random.shuffle(entries)

    # 先頭から順に2つずつペアにする
    for i in range(0, len(entries) - 1, 2):
        pair = (entries[i], entries[i + 1])
        list_pair.append(pair)

# 3. 結果の表示（例：最初の5ペア）
print(f"生成されたペア数: {len(list_pair)}")
for i, (a, b) in enumerate(list_pair[:5]):
    print(f"ペア {i+1}:")
    print(f"  A: id={a['id']}, label={a['label']}")
    print(f"  B: id={b['id']}, label={b['label']}")
    print()

