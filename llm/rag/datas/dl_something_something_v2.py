import os
import shutil
import json

# 例として、前回のコードで生成した list_pair をそのまま使う
# （実際には既に list_pair が定義されている前提）

# 動画が格納されている元ディレクトリ
SRC_VIDEO_DIR = "/content/20bn-something-something-v2"

# ペア保存用のルートディレクトリ
PAIR_ROOT_DIR = "/content/pair_something"

# 保存先ディレクトリを作成
os.makedirs(PAIR_ROOT_DIR, exist_ok=True)

# ペアごとにフォルダを作成し、動画をコピー
for i, (a, b) in enumerate(list_pair):
    # ペア用のサブフォルダ名（例: pair_0001, pair_0002, ...）
    pair_dir = os.path.join(PAIR_ROOT_DIR, f"pair_{i+1:04d}")
    os.makedirs(pair_dir, exist_ok=True)

    # 動画ファイル名（{id}.webm）
    video_a = f"{a['id']}.webm"
    video_b = f"{b['id']}.webm"

    # 元パス
    src_a = os.path.join(SRC_VIDEO_DIR, video_a)
    src_b = os.path.join(SRC_VIDEO_DIR, video_b)

    # コピー先パス
    dst_a = os.path.join(pair_dir, video_a)
    dst_b = os.path.join(pair_dir, video_b)

    # ファイルが存在するか確認してコピー
    if os.path.exists(src_a):
        shutil.copy2(src_a, dst_a)
        print(f"Copied: {src_a} -> {dst_a}")
    else:
        print(f"Warning: Source file not found: {src_a}")

    if os.path.exists(src_b):
        shutil.copy2(src_b, dst_b)
        print(f"Copied: {src_b} -> {dst_b}")
    else:
        print(f"Warning: Source file not found: {src_b}")

    # 必要に応じて、ペアのメタ情報も JSON で保存
    meta = {
        "pair_index": i + 1,
        "label": a["label"],  # a と b は同じ label のはず
        "id_a": a["id"],
        "id_b": b["id"],
        "template_a": a.get("template", ""),
        "template_b": b.get("template", ""),
        "placeholders_a": a.get("placeholders", []),
        "placeholders_b": b.get("placeholders", []),
    }
    meta_path = os.path.join(pair_dir, "meta.json")
    with open(meta_path, "w", encoding="utf-8") as f:
        json.dump(meta, f, indent=2, ensure_ascii=False)

print("Finished copying paired videos.")

